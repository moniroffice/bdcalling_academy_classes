// 1. Print message in the console
console.log("Hello, JavaScript!");

// 2. Show an alert popup
alert("Welcome to JavaScript!");

// 3. Variables (let, const, var)
let name = "Monir";
const age = 25;
var country = "Bangladesh";

console.log("Name:", name);
console.log("Age:", age);
console.log("Country:", country);

// 4. Simple math
let x = 5;
let y = 10;
let sum = x + y;
console.log("Sum of x + y =", sum);

// 5. Function example
function greet(user) {
  console.log("Hello, " + user + "!");
}
greet("Student");

// 6. Condition example
let score = 85;
if (score >= 80) {
  console.log("Grade: A");
} else {
  console.log("Grade: B or below");
}

// 7. Loop example
for (let i = 1; i <= 5; i++) {
  console.log("Number:", i);
}

// 8. Array example
let fruits = ["Apple", "Banana", "Mango"];
console.log("First fruit:", fruits[0]);
console.log("All fruits:", fruits);

// 9. Object example
let student = {
  name: "Rahim",
  age: 20,
  grade: "A"
};
console.log("Student Info:", student);

